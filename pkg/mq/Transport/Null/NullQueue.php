<?php
namespace Formapro\MessageQueue\Transport\Null;

use Formapro\Fms\Queue;

class NullQueue implements Queue
{
    /**
     * @var string
     */
    private $name;

    /**
     * @param string $name
     */
    public function __construct($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getQueueName()
    {
        return $this->name;
    }
}
